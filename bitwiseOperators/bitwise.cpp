#include<iostream>
using namespace std;

int main(){
    int a= 4;
    int b=5; 
    cout<<"a&b "<<(a&b)<<endl;
    cout<<"a|b "<<(a|b)<<endl;
    cout<<"~b "<<(~b)<<endl;
    cout<<"a^b "<<(a^b)<<endl;
    cout<<"17<<2 "<<(17<<2)<<endl;
    cout<<"17>>2 "<<(17>>2)<<endl;

    int i= 7;
    cout<<++i<<endl;
    cout<<i++<<endl;
    cout<<--i<<endl;
    cout<<i--<<endl;
    cout<<i<<endl;
    
}