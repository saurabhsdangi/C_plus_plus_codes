#include<iostream>
using namespace std;
int main()
{
// arithmetic operators
  int a= 6, b=3;
  cout<<a+b<<endl;
  cout<<a-b<<endl;
  cout<<a*b<<endl;
  cout<<a/b<<endl;
  cout<<a%b<<endl;

// relational operators
  cout<<(a==b)<<endl;
  cout<<(a!=b)<<endl;
  cout<<(a<=b)<<endl;
  cout<<(a>=b)<<endl;

// logical operators
  bool x1= true;
  bool x2= false;

  cout<<(x1&&x2)<<endl;
  cout<<(x1||x2)<<endl;
  cout<<(!x2)<<endl;

}